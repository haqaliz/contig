def test_primary_missing_sibling_one_unverified(tmp_path):
    # primary .mtx present but its barcodes sibling is missing -> exactly ONE
    # sc_count_concordance UNVERIFIED (not [], not a crash, never a false pass).
    (tmp_path / "a").mkdir()
    primary = _write_triplet(
        tmp_path / "a",
        genes=[f"gene{i:02d}" for i in range(12)],
        cells=["cell1"],
        counts=[(i + 1, 1, (i + 1) * 100) for i in range(12)],
        write_barcodes=False,
    )
    second = _write_dense_counts(
        tmp_path / "second.tsv",
        {f"gene{i:02d}": (i + 1) * 100 for i in range(12)},
    )

    results = evaluate_sc_count_concordance(primary, second, assay="scrnaseq")

    assert len(results) == 1
    assert results[0].check == "sc_count_concordance"
    assert results[0].status == "unverified"
    assert results[0].value is None
    assert "matrix.mtx" in results[0].message
