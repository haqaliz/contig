def test_genes_as_rows_sum(tmp_path):
    # 3 genes x 2 cells, genes are rows. Pseudobulk = row sum across cells.
    mtx = _write_triplet(
        tmp_path,
        genes=["geneA", "geneB", "geneC"],
        cells=["cell1", "cell2"],
        counts=[(1, 1, 10), (1, 2, 5), (2, 1, 3), (3, 2, 7)],
    )

    assert load_mtx_pseudobulk(mtx) == {"geneA": 15.0, "geneB": 3.0, "geneC": 7.0}


