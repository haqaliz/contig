# --- evaluate_sc_count_concordance: gated entry point ------------------------


def _mtx_pseudobulk(d, mapping, *, gene_axis="rows"):
    """A single-cell triplet whose per-gene pseudobulk equals `mapping`."""
    d.mkdir(parents=True, exist_ok=True)
    genes = list(mapping)
    counts = [(i + 1, 1, mapping[g]) for i, g in enumerate(genes)]
    return _write_triplet(d, genes=genes, cells=["cell1"], counts=counts,
                          gene_axis=gene_axis)


def test_concordant_pair_passes(tmp_path):
    mapping = {f"gene{i:02d}": (i + 1) * 100 for i in range(12)}
    primary = _mtx_pseudobulk(tmp_path / "a", mapping)
    (tmp_path / "b").mkdir()
    second = _mtx_pseudobulk(tmp_path / "b", dict(mapping))

    results = {r.check: r for r in
               evaluate_sc_count_concordance(primary, second, assay="scrnaseq")}

    assert results["spearman_concordance"].status == "pass"
    assert results["spearman_concordance"].value == 1.0


def test_divergent_pair_warns(tmp_path):
    a_map = {f"gene{i:02d}": (i + 1) * 100 for i in range(12)}
    b_map = {f"gene{i:02d}": (12 - i) * 100 for i in range(12)}
    primary = _mtx_pseudobulk(tmp_path / "a", a_map)
    (tmp_path / "b").mkdir()
    second = _mtx_pseudobulk(tmp_path / "b", b_map)

    results = {r.check: r for r in
               evaluate_sc_count_concordance(primary, second, assay="scrnaseq")}
    spearman = results["spearman_concordance"]

    assert spearman.status == "warn"
    assert spearman.value is not None and spearman.value < 0.90


