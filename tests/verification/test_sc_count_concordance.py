def _write_text(path, text, gz):
    if gz:
        with gzip.open(path, "wt") as fh:
            fh.write(text)
    else:
        path.write_text(text)
    return path


def _write_triplet(
    d,
    genes,
    cells,
    counts,
    *,
    gene_axis="rows",
    features_gz=False,
    barcodes_gz=False,
    mtx_gz=False,
    write_barcodes=True,
    comment=False,
):
    """Render a 10x-style triplet (matrix.mtx, features.tsv, barcodes.tsv).

    - genes: list of feature specs; a str -> single-column feature line, a tuple
      -> tab-joined (id, name, type) with column 1 the gene id.
    - cells: list of barcode strings.
    - counts: list of (gene_index, cell_index, value) triplets, 1-based, where
      gene_index/cell_index are logical (independent of orientation). With
      gene_axis="rows" genes are mtx rows; with "cols" the matrix is transposed so
      the same counts must yield the same per-gene pseudobulk totals.
    Returns the matrix.mtx path.
    """
    # features.tsv
    feat_lines = []
    for g in genes:
        feat_lines.append("\t".join(g) if isinstance(g, tuple) else str(g))
    feat_name = "features.tsv.gz" if features_gz else "features.tsv"
    _write_text(d / feat_name, "".join(l + "\n" for l in feat_lines), features_gz)

    # barcodes.tsv
    if write_barcodes:
        bc_name = "barcodes.tsv.gz" if barcodes_gz else "barcodes.tsv"
        _write_text(d / bc_name, "".join(c + "\n" for c in cells), barcodes_gz)

    # matrix.mtx
    if gene_axis == "rows":
        nrows, ncols = len(genes), len(cells)
    else:
        nrows, ncols = len(cells), len(genes)
    mtx_lines = ["%%MatrixMarket matrix coordinate integer general"]
    if comment:
        mtx_lines.append("% generated by a synthetic test fixture")
    mtx_lines.append(f"{nrows} {ncols} {len(counts)}")
    for gi, ci, val in counts:
        r, c = (gi, ci) if gene_axis == "rows" else (ci, gi)
        mtx_lines.append(f"{r} {c} {val}")
    mtx_name = "matrix.mtx.gz" if mtx_gz else "matrix.mtx"
    mtx_path = _write_text(
        d / mtx_name, "".join(l + "\n" for l in mtx_lines), mtx_gz
    )
    return mtx_path


def _write_dense_counts(path, mapping):
    """A one-sample-column dense pseudobulk TSV, no header."""
    path.write_text("".join(f"{g}\t{v}\n" for g, v in mapping.items()))
    return path


